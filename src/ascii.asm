; Behavior implemented:
;   - Print a compact table for the 7-bit ASCII character set, values 0-127.
;   - Each row shows decimal value, hexadecimal value, and a printable character
;     or standard control-code name.
;
; Unsupported behavior:
;   - Option parsing, alternate table formats, locale-aware character names,
;     Unicode, --help, and --version are not implemented.
;
; Syscalls used:
;   - write(2) for stdout.
;   - exit(2) for the process status.
;
; Error handling:
;   - A failed or short write exits with status 1. Extra arguments are ignored
;     in this first static-table teaching version.
;
; Exit behavior:
;   - Exits 0 after writing the complete table; exits 1 on write failure.
;
; Compatibility notes:
;   - This is an educational static 7-bit ASCII reference, not a POSIX-required
;     applet. It deliberately avoids libc and terminal formatting dependencies.

